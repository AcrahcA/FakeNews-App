package fakeNews.modelo;

public class TraducionAudioVisual {
	
	private String nombreDelPrograna;
	private String minutosDedicados;
	private String rating;
	public String getNombreDelPrograna() {
		return nombreDelPrograna;
	}
	public void setNombreDelPrograna(String nombreDelPrograna) {
		this.nombreDelPrograna = nombreDelPrograna;
	}
	public String getMinutosDedicados() {
		return minutosDedicados;
	}
	public void setMinutosDedicados(String minutosDedicados) {
		this.minutosDedicados = minutosDedicados;
	}
	public String getRating() {
		return rating;
	}
	public void setRating(String rating) {
		this.rating = rating;
	}

}
	
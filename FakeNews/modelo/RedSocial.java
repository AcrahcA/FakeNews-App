package fakeNews.modelo;

public class RedSocial {
	
	private String plataforma;
	private String usuario;
	private String numeroDeInteracciones;
	public String getPlataforma() {
		return plataforma;
	}
	public void setPlataforma(String plataforma) {
		this.plataforma = plataforma;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getNumeroDeInteracciones() {
		return numeroDeInteracciones;
	}
	public void setNumeroDeInteracciones(String numeroDeInteracciones) {
		this.numeroDeInteracciones = numeroDeInteracciones;
	}
	
	
	
	

    
}

package fakeNews.modelo;


public class Refutacion {
	private String fecha;
	private String fuentes;
	private String organismo_oficial;
	private String refutador;
	
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getFuentes() {
		return fuentes;
	}
	public void setFuentes(String fuentes) {
		this.fuentes = fuentes;
	}
	public String getOrganismo_oficial() {
		return organismo_oficial;
	}
	public void setOrganismo_oficial(String organismo_oficial) {
		this.organismo_oficial = organismo_oficial;
	}
	public String getRefutador() {
		return refutador;
	}
	public void setRefutador(String refutador) {
		this.refutador = refutador;
	}
	
	
    
}

package fakeNews.modelo;

public class Refutador {
	private String nombre;
	private String apellido;
	private String medios_presentes;
	private int legajo;
	
	

	public int getLegajo() {
		return legajo;
	}



	public void setLegajo(int legajo) {
		this.legajo = legajo;
	}

	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	public String getMedios_presentes() {
		return medios_presentes;
	}
	public void setMedios_presentes(String medios_presentes) {
		this.medios_presentes = medios_presentes;
	}



	
	
	
	
	

}
